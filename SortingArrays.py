import numpy as np
ab = np.array([5, 3, 4, 0, 2, 1])
print(np.sort(ab))

# Sorting alphabetically
ac = np.array(['banana', 'apple', 'cucumber', 'mango', 'cherry'])
print(np.sort(ac))

# Sorting boolean array
ad = np.array([True, False, True, False, False])
print(np.sort(ad))

# Sorting 2-D array
ae = np.array([[21, 10, 3], [8, -6, 4], [3, 0, -2]])
print(np.sort(ae))


